#Nos permiten realizar operaciones varias

sum = 10 + 10
res = 10 - 5
mul = 2*5
div = 10/2
#Tambien esta modulo, lo cual es el reciduo de la division
mod = 10%2
#Si queremos solo el entero de un float usamos este, el cual divide y deja solo el entero
ent= 10//3
#Potencia se haria de esta forma donde el exponente es el segundo numero
exp = 2**3

print("La suma da {}, la resta da {}, la multriplicacion da {}, la division da {}, el modulo es {}, "
      "el entero es {}, la potencia es {}".format(sum, res, mul, div, mod, ent, exp))

