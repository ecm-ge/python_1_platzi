#Indexamientos de las cadenas de texto

#Asi buscamos letra a letra
text = 'Ella sabe python'
print(text[0])

#Hacer conteo de caracteres haca atras
print(text[-1])

#Similar a un split-slice
print(text[0:5])

#Tomar datos de inicio hasta el caracter dicho
print(text[:10])

#Toma de posicion dada al final
print(text[3:])