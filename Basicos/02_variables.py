#Variable numerica
my_age = 28
print(my_age)

#En python las variables pueden asumir cualquier valor
my_name='Yair'
print(my_name)

#Solicitar datos por consola
my_name = input("¿Cual es tu nombre? ")
print(my_name)

my_age = input("¿Que edad tienes?")
print(my_age)